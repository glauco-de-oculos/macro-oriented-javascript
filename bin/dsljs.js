#!/usr/bin/env node
import fs from "fs";
import path from "path";
import chokidar from "chokidar";
import vm from "node:vm";
import { createRequire } from "node:module";
import process from "node:process";
import { spawn } from "node:child_process";

import {
  stripMacrosBlock,
  parseMacrosFromBlock,
  expandMacros,
  compileDslFile
} from "../src/parser.js";

let child = null;
let currentContext = null;

function expandInputFile(inputFile) {
  return compileDslFile(inputFile);
}

function writeExpandedFile(outputFile, expanded) {
  fs.mkdirSync(path.dirname(outputFile), { recursive: true });
  fs.writeFileSync(outputFile, expanded, "utf8");
}

function getShadowOutputPath(inputFile) {
  if (inputFile.endsWith(".dsljs")) {
    return inputFile.slice(0, -".dsljs".length) + ".generated.js";
  }

  if (inputFile.endsWith(".dsl.js")) {
    return inputFile.slice(0, -".dsl.js".length) + ".generated.js";
  }

  return `${inputFile}.generated.js`;
}

function buildShadowFileContents(inputFile, expanded) {
  const normalizedInput = inputFile.replace(/\\/g, "/");
  return [
    "// @ts-check",
    `// Generated from: ${normalizedInput}`,
    "// Do not edit this file directly.",
    "",
    expanded.trim(),
    ""
  ].join("\n");
}

function runSpawn(inputFile, argv = []) {
  const expanded = expandInputFile(inputFile);
  const tmpFile = path.join(
    path.dirname(inputFile),
    `.${path.basename(inputFile)}.dsljs.tmp.mjs`
  );

  fs.writeFileSync(tmpFile, expanded, "utf8");

  if (child) {
    child.kill("SIGTERM");
    child = null;
  }

  console.log("executing (spawn, ESM)...");

  child = spawn(process.execPath, [tmpFile, ...argv], {
    stdio: "inherit",
    env: process.env
  });

  child.on("exit", code => {
    if (code !== null) {
      console.log("exited:", code);
    }
  });
}

function runInVm(inputFile, argv = []) {
  const expanded = expandInputFile(inputFile);
  const filename = path.resolve(inputFile);
  const dirname = path.dirname(filename);
  const require = createRequire(filename);

  if (currentContext?.__dispose) {
    try {
      currentContext.__dispose();
    } catch {}
  }

  const context = {
    console,
    require,
    __filename: filename,
    __dirname: dirname,
    module: { exports: {} },
    exports: {},
    process: {
      ...process,
      argv: [process.argv[0], filename, ...argv],
      exit: () => {}
    },
    __dispose: null
  };

  vm.createContext(context);

  Object.defineProperty(context, "__dispose", {
    value: () => {
      if (context.module?.exports?.dispose) {
        context.module.exports.dispose();
      }
    }
  });

  currentContext = context;

  const script = new vm.Script(expanded, {
    filename,
    displayErrors: true
  });

  console.log("executing...");
  script.runInContext(context);
  console.log("execution finished");
}

function runFile(inputFile, argv = []) {
  const expanded = expandInputFile(inputFile);
  const filename = path.resolve(inputFile);
  const dirname = path.dirname(filename);
  const require = createRequire(filename);

  const context = {
    console,
    process: {
      ...process,
      argv: [process.argv[0], filename, ...argv]
    },
    require,
    __filename: filename,
    __dirname: dirname,
    module: { exports: {} },
    exports: {}
  };

  vm.createContext(context);

  const script = new vm.Script(expanded, {
    filename,
    displayErrors: true
  });

  script.runInContext(context);
}

function compileFile(inputFile, outputFile) {
  const expanded = expandInputFile(inputFile);
  writeExpandedFile(outputFile, expanded);
  console.log("compiled", inputFile, "->", outputFile);
}

function writeShadowFile(inputFile, outputFile = getShadowOutputPath(inputFile)) {
  const expanded = expandInputFile(inputFile);
  const shadow = buildShadowFileContents(inputFile, expanded);
  writeExpandedFile(outputFile, shadow);
  console.log("shadow", inputFile, "->", outputFile);
}

function isDslFile(file) {
  return file.endsWith(".dsljs") || file.endsWith(".dsl.js");
}

function isFile(target) {
  return fs.existsSync(target) && fs.statSync(target).isFile();
}

function isDir(target) {
  return fs.existsSync(target) && fs.statSync(target).isDirectory();
}

function distDir(srcDir, outDir) {
  const files = fs.readdirSync(srcDir, { recursive: true });

  for (const file of files) {
    const full = path.join(srcDir, file);
    if (!isDslFile(full)) continue;

    const rel = path.relative(srcDir, full)
      .replace(/\.dsljs$/u, ".js")
      .replace(/\.dsl\.js$/u, ".js");
    const out = path.join(outDir, rel);

    compileFile(full, out);
  }
}

function watchDir(srcDir, outDir) {
  console.log("watching:", srcDir);

  chokidar.watch(srcDir, { ignoreInitial: false })
    .on("add", file => {
      if (!isDslFile(file)) return;
      const rel = path.relative(srcDir, file)
        .replace(/\.dsljs$/u, ".js")
        .replace(/\.dsl\.js$/u, ".js");
      compileFile(file, path.join(outDir, rel));
    })
    .on("change", file => {
      if (!isDslFile(file)) return;
      const rel = path.relative(srcDir, file)
        .replace(/\.dsljs$/u, ".js")
        .replace(/\.dsl\.js$/u, ".js");
      compileFile(file, path.join(outDir, rel));
    });
}

function watchFile(inputFile, outputFile) {
  console.log("watching file:", inputFile);

  const compile = () => compileFile(inputFile, outputFile);
  compile();

  chokidar.watch(inputFile, { ignoreInitial: true })
    .on("change", () => {
      compile();
    });
}

function watchShadowFile(inputFile, outputFile = getShadowOutputPath(inputFile)) {
  console.log("watching shadow source:", inputFile);

  const write = () => writeShadowFile(inputFile, outputFile);
  write();

  chokidar.watch(inputFile, { ignoreInitial: true })
    .on("change", () => {
      write();
    });
}

function watchRunFile(inputFile, argv = []) {
  console.log("watch-run started:", inputFile);

  const run = () => {
    try {
      runInVm(inputFile, argv);
    } catch (err) {
      console.error("runtime error\n", err);
    }
  };

  run();

  chokidar.watch(inputFile, { ignoreInitial: true })
    .on("change", () => {
      console.log("restarting...");
      run();
    });
}

function watchRunSpawn(inputFile, argv = []) {
  console.log("watch-run (spawn) started:", inputFile);

  const run = () => {
    try {
      runSpawn(inputFile, argv);
    } catch (err) {
      console.error("runtime error\n", err);
    }
  };

  run();

  chokidar.watch(inputFile, { ignoreInitial: true })
    .on("change", () => {
      console.log("restarting...");
      run();
    });
}

function printUsage() {
  console.error("Usage:");
  console.error("  dsljs <input.dsljs> [output.js]");
  console.error("  dsljs dist <srcDir> <outDir>");
  console.error("  dsljs watch <src> <out>");
  console.error("  dsljs run <file.dsljs> [...args]");
  console.error("  dsljs watch-run <file.dsljs> [...args]");
  console.error("  dsljs shadow <file.dsljs> [file.generated.js]");
  console.error("  dsljs watch-shadow <file.dsljs> [file.generated.js]");
}

function main() {
  const [, , cmd, a, b] = process.argv;

  if (!cmd) {
    printUsage();
    process.exit(1);
  }

  if (cmd === "dist") {
    if (!a || !b) {
      console.error("Usage: dsljs dist <srcDir> <outDir>");
      process.exit(1);
    }

    distDir(a, b);
    process.exit(0);
  }

  if (cmd === "watch") {
    if (!a || !b) {
      console.error("Usage: dsljs watch <src> <out>");
      process.exit(1);
    }

    if (isFile(a)) {
      watchFile(a, b);
      return;
    }

    if (isDir(a)) {
      watchDir(a, b);
      return;
    }

    console.error("Invalid path:", a);
    process.exit(1);
  }

  if (cmd === "watch-run") {
    if (!a) {
      console.error("Usage: dsljs watch-run <file.dsljs> [...args]");
      process.exit(1);
    }

    watchRunSpawn(a, process.argv.slice(4));
    return;
  }

  if (cmd === "run") {
    if (!a) {
      console.error("Usage: dsljs run <file.dsljs> [...args]");
      process.exit(1);
    }

    runFile(a, process.argv.slice(4));
    return;
  }

  if (cmd === "shadow") {
    if (!a) {
      console.error("Usage: dsljs shadow <file.dsljs> [file.generated.js]");
      process.exit(1);
    }

    writeShadowFile(a, b);
    return;
  }

  if (cmd === "watch-shadow") {
    if (!a) {
      console.error("Usage: dsljs watch-shadow <file.dsljs> [file.generated.js]");
      process.exit(1);
    }

    watchShadowFile(a, b);
    return;
  }

  const input = cmd;
  const output = a;
  const expanded = compileDslFile(input);

  if (output) {
    writeExpandedFile(output, expanded);
  } else {
    console.log(expanded);
  }
}

main();
